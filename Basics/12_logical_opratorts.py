# Logical Operators العوامل المنطقية
# and, or, not

username = "admin"
password = "12345"

print(username == "admin" and password == "12345") # في هذه الحالة يجب ان تتحقق جميع الشروط
print(username == "user" or password == "12345") # في هذه الحالة يجب ان يتحقق احد الشروط

boolean = True

print(not boolean) # يتم عكس القيمة