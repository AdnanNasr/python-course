# Escape sequences: Use backslashes (`\`) to include special characters like newlines (`\n`) or tabs (`\t`) in strings.
# Raw String r""


print("Hello, \"Python\"") # escape double quotes

print("Hello,\nPython") # new line using \n

print ("Hello\tPython") # tap using \t

path = r"C:\path\to\file" # raw string using r""
