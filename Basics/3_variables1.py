# Variables in Python

# A variable is like a Box that holds data.
# Variables make it easy to store, update, and use data in your programs.
# - Variable names should be descriptive and meaningful.
# - They must start with a letter or an underscore (_), but not a number.
# - They can only contain letters, numbers, and underscores.
# - Python is case-sensitive, so 'Name' and 'name' are different variables.

